package flowcontrolstatements;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		
		char x='b';
		switch(x)
		{
		case 'a': System.out.println("Hi");
		            break;
		            
		case 'b': System.out.println("Hello");
        			break;
        default: System.out.println("bye");		
		}

	}

}
