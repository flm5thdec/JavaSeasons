package operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		int x=10;
		
		//System.out.println(x=x+1);
		
		System.out.println(x+=10);
		
		System.out.println(x-=10);
		
		System.out.println(x/=10);
		
		System.out.println(x%10);
		
		System.out.println(x*=10);

	}

}
